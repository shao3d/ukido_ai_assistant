        # –ü—ã—Ç–∞–µ–º—Å—è –ø–æ–ª—É—á–∏—Ç—å —Å–æ–µ–¥–∏–Ω–µ–Ω–∏—è —Å Pinecone
        index_facts, index_style = get_pinecone_indexes()
        
